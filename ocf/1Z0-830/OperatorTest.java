class OperatorTest{
	public static void main(String[] args){
		System.out.printf("%d+%d=%d\n",3,8,3+8);
		System.out.printf("%d-%d=%d\n",9,2,9-2);
		System.out.printf("%f*%d=%f\n",8.1,5,8.1*5);
		System.out.printf("%d/%d=%d\n",9,4,9/4);
		System.out.printf("%f/%d=%f\n",9.0,4,9.0/4);
		System.out.printf("%d/%f=%f\n",9,4.1,9/4.1);
	}
}
